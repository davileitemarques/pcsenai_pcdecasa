#Peça para o usuario o valor de cada quociente
a = float(input("Digite o valor do quociente A: "))
b = float(input("Digite o valor do quociente B: "))
c = float(input("Digite o valor do quociente C: "))
#Calcule o delta
delta = (b ** 2 - 4 * a * c)
#Imprima o resultado
print(f"Resultado do delta é {delta}")