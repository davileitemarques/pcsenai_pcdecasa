
numero = int(input("Digite um numero: "))

raiz_quadrada = numero ** 0.5
quarta_potencia = numero ** 4
quociente = numero // 2
quociente_final = numero * quociente

print(f"A raiz quadrada desse numero é: {raiz_quadrada}")
print(f"A quarta potencia do numero é: {quarta_potencia}")
print(f"Numero vezes o quociente da divisão por dois: {quociente_final}")